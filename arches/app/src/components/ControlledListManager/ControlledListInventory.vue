<script setup lang="ts">
import arches from "arches";
import Cookies from "js-cookie";
import { ref } from "vue";
import { useGettext } from "vue3-gettext";

import Splitter from "primevue/splitter";
import SplitterPanel from "primevue/splitterpanel";
import { useToast } from "primevue/usetoast";

import ControlledListTable from "@/components/ControlledListManager/ControlledListTable.vue";
import SidepanelDataView from "@/components/ControlledListManager/SidepanelDataView.vue";
import SpinnerIcon from "@/components/SpinnerIcon.vue";

import type { Ref } from "vue";
import type {
    ControlledList,
    LanguageMap,
} from "@/types/ControlledListManager";

const props: {
    displayedList: ControlledList | null;
    languageMap: LanguageMap | null;
    setDisplayedList: (list: ControlledList | null) => void;
    setLanguageMap: (map: LanguageMap) => void;
    setEditing: (val: boolean) => void;
} = defineProps([
    "displayedList",
    "languageMap",
    "setDisplayedList",
    "setLanguageMap",
    "setEditing",
]);

const items: Ref<ControlledList[]> = ref([]);
const toast = useToast();
const { $gettext, $ngettext } = useGettext();
const lightGray = "#f4f4f4";
const ERROR = "error";

const fetchLists = async () => {
    let errorText;
    try {
        const response = await fetch(arches.urls.controlled_lists);
        if (!response.ok) {
            errorText = response.statusText;
            const body = await response.json();
            errorText = body.message;
            throw new Error();
        } else {
            await response.json().then((data) => {
                props.setLanguageMap(data.languages);
                items.value = data.controlled_lists;
            });
        }
    } catch {
        toast.add({
            severity: ERROR,
            summary: errorText || $gettext("Unable to fetch lists"),
            life: 3000,
        });
    }
};

const createList = async () => {
    try {
        const response = await fetch(arches.urls.controlled_list_add, {
            method: "POST",
            headers: {
                "X-CSRFToken": Cookies.get("csrftoken"),
            },
        });
        if (response.ok) {
            const newItem = await response.json();
            items.value.unshift(newItem);
        } else {
            throw new Error();
        }
    } catch {
        toast.add({
            severity: ERROR,
            summary: $gettext("List creation failed"),
            life: 3000,
        });
    }
};

const deleteLists = async (selectedItems: ControlledList[]) => {
    if (!selectedItems.length) {
        return;
    }
    const promises = selectedItems.map((list) =>
        fetch(arches.urls.controlled_list(list.id), {
            method: "DELETE",
            headers: {
                "X-CSRFToken": Cookies.get("csrftoken"),
            },
        })
    );

    const shouldResetDisplay = (
        props.displayedList && selectedItems.includes(props.displayedList)
    );

    try {
        const responses = await Promise.all(promises);
        if (responses.some((resp) => resp.ok)) {
            if (shouldResetDisplay) {
                props.setDisplayedList(null);
            }
        }
        if (responses.some((resp) => !resp.ok)) {
            throw new Error();
        }
    } catch {
        toast.add({
            severity: ERROR,
            summary: $gettext("One or more lists failed to delete."),
            life: 3000,
        });
    }
    await fetchLists();
};
</script>

<template>
    <Splitter
        :pt="{
            gutter: { style: { background: lightGray } },
            gutterHandler: { style: { background: lightGray } },
        }"
    >
        <SplitterPanel
            :size="30"
            :min-size="15"
        >
            <div class="header">
                <h2>{{ $gettext('Controlled Lists') }}</h2>
            </div>

            <Suspense>
                <SidepanelDataView
                    :add-label="$gettext('Create New List')"
                    :create-item="createList"
                    :delete-items="deleteLists"
                    :delete-label="$gettext('Delete List')"
                    :delete-label-plural="$gettext('Delete Lists')"
                    :displayed-item="displayedList"
                    :fetch-items="fetchLists"
                    :item-label="$ngettext('list', 'lists', items.length)"
                    :items="items"
                    :no-search-result-label="$gettext('No matching lists.')"
                    :no-item-label="$gettext('Click &quot;Create New List&quot; to start.')"
                    :set-displayed="setDisplayedList"
                />
                <template #fallback>
                    <SpinnerIcon />
                </template>
            </Suspense>
        </SplitterPanel>

        <SplitterPanel
            :size="75"
            :min-size="50"
            class="mt-0"
        >
            <ControlledListTable
                :displayed-list
                :language-map
                :set-editing
            />
        </SplitterPanel>
    </Splitter>
</template>

